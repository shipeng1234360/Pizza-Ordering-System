
public class pizza {
 
	private double price;
	private String size;
	private double topping_fees;
	private String topping;
	
	pizza(){
		
	}
	
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}

	public double getTopping_fees() {
		return topping_fees;
	}

	public void setTopping_fees(double topping_fees) {
		this.topping_fees = topping_fees;
	}

	public String getTopping() {
		return topping;
	}

	public void setTopping(String topping) {
		this.topping = topping;
	}
	
}
